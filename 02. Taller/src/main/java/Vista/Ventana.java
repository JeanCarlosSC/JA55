    private void InsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertarActionPerformed
        String nombre =    textoNombre1.getText();
        String apellido = textoApellido1.getText();
        int id = Integer.parseInt(textoId1.getText());
        if(control.insertarCliente(id, nombre, apellido)) {
            JOptionPane.showMessageDialog(this, "Cliente insertado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            JOptionPane.showMessageDialog(this, "El cliente ya existe", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_InsertarActionPerformed

