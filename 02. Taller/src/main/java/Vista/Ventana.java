    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        int id = Integer.parseInt(textoId2.getText());
        if(control.existeCliente(id)){
            textoNombre2.setText(control.getNombre(id));
            textoApellido2.setText(control.getApellido(id));
        }
        else {
            JOptionPane.showMessageDialog(this, "Cliente no insertado", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BuscarActionPerformed

